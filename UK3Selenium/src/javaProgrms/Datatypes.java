package javaProgrms;

public class Datatypes {

	public static void main(String[] args) {
		
		// Start Coding ...
		
		char ind_char_1='*'; // character data type 2 bytes
		
		int i=10;  // integer data type non-decimal value - 4 Bytes
		
		double d = 10.100000000101234785966506; // double decimal value - 8 bytes, 16 decimal places it can hold
		
		float f =(float) 4.590478690; // flaot decimal value - 4 bytes, 6 decimal places it can hold.
		
		String s ="Selenium"; // String Value
		
		boolean b =false; // boolean 1 bit ,( true or false)
		
		// printing the Values 
		
		System.out.println("Character 'c' :-"+ind_char_1);
		
		System.out.println("Integer 'i' :-"+i);
		
		System.out.println("Double 'd':-"+d);
		
		System.out.println("Float 'f':-"+f);
		
		System.out.println("String 's':-"+s);
		
		System.out.println("Boolean 'b' :-"+b);
	
	}

}
